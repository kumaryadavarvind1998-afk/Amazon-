package com.amzon.request;

public class UserRequest
{
	private String mobile;
	
	private String name;

	public String getMobile()
	{
		return mobile;
	}

	public void setMobile(String mobile) 
	{
		this.mobile = mobile;
	}

	public String getName() 
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}
	
	
}
